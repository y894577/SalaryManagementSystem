package People;

public class Parameters {
    private User user;
    private int classNum;
    private int studentNum;
    private float livingPara;
    private float titlePara;
    private float phonePara;
    private float otherPara;
    private String calculatePara;

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public int getClassNum() {
        return classNum;
    }

    public void setClassNum(int classNum) {
        this.classNum = classNum;
    }

    public int getStudentNum() {
        return studentNum;
    }

    public void setStudentNum(int studentNum) {
        this.studentNum = studentNum;
    }

    public float getLivingPara() {
        return livingPara;
    }

    public void setLivingPara(float livingPara) {
        this.livingPara = livingPara;
    }

    public float getTitlePara() {
        return titlePara;
    }

    public void setTitlePara(float titlePara) {
        this.titlePara = titlePara;
    }

    public float getPhonePara() {
        return phonePara;
    }

    public void setPhonePara(float phonePara) {
        this.phonePara = phonePara;
    }

    public float getOtherPara() {
        return otherPara;
    }

    public void setOtherPara(float otherPara) {
        this.otherPara = otherPara;
    }

    public String getCalculatePara() {
        return calculatePara;
    }

    public void setCalculatePara(String calculatePara) {
        this.calculatePara = calculatePara;
    }
}
